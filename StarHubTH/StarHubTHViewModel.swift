    func saveProfiles() {
        if let data = try? JSONEncoder().encode(modProfiles) {
            UserDefaults.standard.set(data, forKey: "modProfiles")
        }
        if let activeId = activeProfileId {
            UserDefaults.standard.set(activeId.uuidString, forKey: "activeProfileId")
        } else {
            UserDefaults.standard.removeObject(forKey: "activeProfileId")
        }
    }
    
    func createProfile(name: String) {
        // Snapshot the currently enabled mods into the new profile
        let currentEnabledIds = mods
            .flatMap { mod -> [String] in
                if mod.isGroup, let children = mod.children {
                    return children.filter { $0.isEnabled }.map { $0.uniqueId }
                }
                return mod.isEnabled ? [mod.uniqueId] : []
            }
            .filter { !$0.isEmpty }

        let newProfile = ModProfile(name: name, enabledModIds: currentEnabledIds)
        modProfiles.append(newProfile)
        saveProfiles()
        // Do NOT applyProfile here — just save so the user can edit it first
        activeProfileId = newProfile.id
        saveProfiles()
    }
    
    func deleteProfile(id: UUID) {
        modProfiles.removeAll { $0.id == id }
        if activeProfileId == id {
            activeProfileId = nil
        }
        saveProfiles()
    }
    
    func updateProfile(id: UUID, newName: String, enabledModIds: [String]) {
        if let index = modProfiles.firstIndex(where: { $0.id == id }) {
            modProfiles[index].name = newName
            modProfiles[index].enabledModIds = enabledModIds
            saveProfiles()

            // If this is the active profile, apply the new mod selection to the filesystem
            if activeProfileId == id {
                applyProfileToFilesystem(profile: modProfiles[index])
            }
        }
    }

    func applyProfile(id: UUID?) {
        guard let id = id, let profile = modProfiles.first(where: { $0.id == id }) else {
            activeProfileId = nil
            saveProfiles()
            return
        }

        // If already active, just sync stored list from current filesystem (no file moves)
        if activeProfileId == id {
            syncActiveProfileIds()
            return
        }

        activeProfileId = id
        saveProfiles()
        applyProfileToFilesystem(profile: profile)
        self.log(String(format: L(L10n.VM.switchProfile), profile.name))
    }

    /// Actually move mod files to match the given profile's enabledModIds.
    ///
    /// Unlike `toggleMod`, the previous implementation swallowed every
    /// filesystem error with `try?`, so a partial failure (e.g. one mod
    /// folder locked by another process, a permission issue, a stale
    /// destination) left the Mods/Mods_disabled layout in an inconsistent
    /// state with no signal to the user. This version captures each move
    /// error, logs it, and surfaces a user-visible alert summarizing how
    /// many mods could not be relocated — while still rescanning so the UI
    /// reflects the actual on-disk state (whatever it is).
    private func applyProfileToFilesystem(profile: ModProfile) {
        let fm = FileManager.default
        let modsPath = (gameDir as NSString).appendingPathComponent("Mods")
        let disabledModsPath = (gameDir as NSString).appendingPathComponent("Mods_disabled")

        // Records each mod that could not be moved, with the underlying
        // error for the log. Drives both the user-facing alert and the
        // per-mod log lines below.
        struct MoveFailure {
            let modName: String
            let direction: String   // "→ Mods" / "→ Mods_disabled"
            let error: Error
        }
        var failures: [MoveFailure] = []
        var attempted = 0
        var anyEnabled = false

        // Shared helper: ensure the destination parent directory exists,
        // then move the folder. Returns nil on success, the error on
        // failure — never throws so the loop can keep processing the
        // remaining mods instead of aborting at the first error.
        func moveModFolder(_ mod: ModItem, from src: String, to dst: String, direction: String) {
            attempted += 1
            let dstParent = (dst as NSString).deletingLastPathComponent
            do {
                if !fm.fileExists(atPath: dstParent) {
                    try fm.createDirectory(atPath: dstParent, withIntermediateDirectories: true, attributes: nil)
                }
                try fm.moveItem(atPath: src, toPath: dst)
            } catch {
                failures.append(MoveFailure(modName: mod.name, direction: direction, error: error))
            }
        }

        // Check whether a mod (or any of its children) is covered by the profile's enabled list
        func isCoveredByProfile(_ mod: ModItem) -> Bool {
            if mod.isGroup, let children = mod.children {
                return children.contains { profile.enabledModIds.contains($0.uniqueId) }
            }
            return profile.enabledModIds.contains(mod.uniqueId)
        }

        // Disable mods not in profile
        for mod in mods.filter({ $0.isEnabled }) {
            guard !isCoveredByProfile(mod) else { continue }
            let src = (modsPath as NSString).appendingPathComponent(mod.folderName)
            let dst = (disabledModsPath as NSString).appendingPathComponent(mod.folderName)
            moveModFolder(mod, from: src, to: dst, direction: "→ Mods_disabled")
        }

        // Enable mods in profile. Only stamp the activation timestamp for
        // mods that were actually moved — stamping a mod that failed to
        // move would record a phantom "last activation" for a folder that
        // is still sitting in Mods_disabled.
        for mod in mods.filter({ !$0.isEnabled }) {
            guard isCoveredByProfile(mod) else { continue }
            let src = (disabledModsPath as NSString).appendingPathComponent(mod.folderName)
            let dst = (modsPath as NSString).appendingPathComponent(mod.folderName)
            let beforeCount = failures.count
            moveModFolder(mod, from: src, to: dst, direction: "→ Mods")
            if failures.count == beforeCount {
                self.modActivationTimestamps[mod.folderName] = Date()
                anyEnabled = true
            }
        }
        if anyEnabled {
            Self.saveModActivationTimestamps(self.modActivationTimestamps)
        }

        // Log each failure individually so the Logs tab shows exactly
        // which mod(s) and why — the aggregated alert only gives a count.
        for failure in failures {
            log(
                String(format: "%@ %@: %@",
                       failure.modName, failure.direction, failure.error.localizedDescription),
                level: .error
            )
        }

        // Always rescan so the list reflects the real on-disk state,
        // whatever it is after partial failures. syncActiveProfileIds
        // runs after so the active profile's stored id list tracks the
        // actual enabled set (possibly fewer than expected if moves
        // failed).
        DispatchQueue.global(qos: .userInitiated).async {
            self.scanMods()
            DispatchQueue.main.async {
                self.syncActiveProfileIds()
                // Surface the outcome to the user. A partial application
                // is the dangerous case: the profile is "active" but the
                // filesystem doesn't fully match it, so the next toggle
                // cycle could compound the inconsistency. Tell the user
                // explicitly rather than letting it look like success.
                if !failures.isEmpty {
                    let profileName = profile.name
                    if attempted == failures.count {
                        // Every move failed — profile was not applied at all.
                        self.showModal(message: String(format: self.L(L10n.VM.applyProfileError),
                                                       profileName, failures.count))
                    } else {
                        // Some moves succeeded, some failed.
                        self.showModal(message: String(format: self.L(L10n.VM.applyProfilePartial),
                                                       profileName, failures.count))
                    }
                    self.log(String(format: "Profile \"%@\" applied with %lld failure(s)",
                                    profileName, failures.count), level: .warning)
                }
            }
        }
    }

    /// Compute which uniqueIds should be added/removed when toggling a mod in a profile,
    /// using the same chain logic as toggleMod. Works on an in-memory set (no file I/O).
    /// - Parameters:
    ///   - mod: The mod being toggled (can be a group or single mod)
    ///   - enable: true = enabling, false = disabling
    ///   - currentEnabled: the current set of enabled uniqueIds in the profile
    /// - Returns: A new set with the chain applied
    func applyChainToSet(mod: ModItem, enable: Bool, currentEnabled: Set<String>) -> Set<String> {
        var result = currentEnabled

        // Build helpers identical to toggleMod
        func getTopLevelMod(for uniqueId: String) -> ModItem? {
            for m in mods {
                if !m.isGroup && m.uniqueId.caseInsensitiveCompare(uniqueId) == .orderedSame { return m }
                if m.isGroup, let children = m.children,
                   children.contains(where: { $0.uniqueId.caseInsensitiveCompare(uniqueId) == .orderedSame }) { return m }
            }
            return nil
        }

        func getDependencies(for topMod: ModItem) -> [ModDependency] {
            if topMod.isGroup, let children = topMod.children { return children.flatMap { $0.dependencies } }
            return topMod.dependencies
        }

        // Collect all uniqueIds provided by the target mod (single or group)
        func providedIds(for topMod: ModItem) -> [String] {
            if topMod.isGroup, let children = topMod.children { return children.map { $0.uniqueId } }
            return [topMod.uniqueId]
        }

        // All top-level mods to process — for a group, start with the group itself
        let startingMod = mod.isGroup ? mod : (getTopLevelMod(for: mod.uniqueId) ?? mod)
        let startingIds = providedIds(for: startingMod)

        if enable {
            // Enable starting mod's ids
            startingIds.forEach { result.insert($0) }

            if chainToggleDependencies {
                // BFS: enable all required dependencies
                var queue = [startingMod]
                var visited = Set<String>([startingMod.folderName])
                while !queue.isEmpty {
                    let current = queue.removeFirst()
                    for dep in getDependencies(for: current) where dep.isRequired {
                        if let depMod = getTopLevelMod(for: dep.uniqueId), !visited.contains(depMod.folderName) {
                            visited.insert(depMod.folderName)
                            providedIds(for: depMod).forEach { result.insert($0) }
                            queue.append(depMod)
                        }
                    }
                }
            }
        } else {
            // Disable starting mod's ids
            startingIds.forEach { result.remove($0) }

            if chainToggleDependencies {
                // BFS: disable any mod that requires the now-disabled mod
                var queue = [startingMod]
                var visited = Set<String>([startingMod.folderName])
                while !queue.isEmpty {
                    let current = queue.removeFirst()
                    let currentIds = providedIds(for: current)
                    for candidate in mods {
                        guard !visited.contains(candidate.folderName) else { continue }
                        // Only cascade through mods that are *currently*
                        // enabled in this set — mirrors `performToggle`'s
                        // disable branch, which is gated on
                        // `otherMod.isEnabled`. Without this, an
                        // already-disabled candidate that merely lists a
                        // dependency on the mod being disabled would get its
                        // ids incorrectly removed from `result` even though
                        // it was never enabled to begin with.
                        let candidateIsEnabled = providedIds(for: candidate).contains { currentEnabled.contains($0) }
                        guard candidateIsEnabled else { continue }
                        let candidateDeps = getDependencies(for: candidate)
                        let requiresCurrent = candidateDeps.contains { dep in
                            dep.isRequired && currentIds.contains { $0.caseInsensitiveCompare(dep.uniqueId) == .orderedSame }
                        }
                        if requiresCurrent {
                            visited.insert(candidate.folderName)
                            providedIds(for: candidate).forEach { result.remove($0) }
                            queue.append(candidate)
                        }
                    }
                }
            }
        }

        return result
    }
    /// Call this after any toggleMod so the profile stays up to date.
    func syncActiveProfileIds() {
        guard let id = activeProfileId,
              let index = modProfiles.firstIndex(where: { $0.id == id }) else { return }

        let actualEnabledIds = mods
            .flatMap { mod -> [String] in
                if mod.isGroup, let children = mod.children {
                    return children.filter { $0.isEnabled }.map { $0.uniqueId }
                }
                return mod.isEnabled ? [mod.uniqueId] : []
            }
            .filter { !$0.isEmpty }

        modProfiles[index].enabledModIds = actualEnabledIds
        saveProfiles()
    }
