    func loadProfiles() {
        if let data = UserDefaults.standard.data(forKey: "modProfiles"),
           let profiles = try? JSONDecoder().decode([ModProfile].self, from: data) {
            self.modProfiles = profiles
        } else {
            self.modProfiles = []
